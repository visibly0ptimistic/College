import java.io.*;
import java.util.*;

public class RelationalOperators {
  public static void main(String[] args) {

    // data dictionary
    int value1 = 1;
    int value2 = 2;

    if(value1 == value2)
      System.out.println("value1 == value2");

    if(value1 != value2)
      System.out.println("value1 != value2");

    if(value1 > value2)
      System.out.println("value1 > value2");

    if(value1 < value2)
      System.out.println("value1 < value2");

    if(value1 <= value2)
      System.out.println("value1 <= value2");

  } // end main
} // end RelationalOperators
